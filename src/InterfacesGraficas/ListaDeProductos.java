    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(!"".equals(txtBuscar.getText())){
            buscarEvent();
            if(modeloLista.isEmpty())
                mostrarMensaje("No se encontraron productos con ese nombre");
            else 
                mostrarMensaje("Se encontraron los siguientes productos");
        }
        else {
            llenarLista();
            JOptionPane.showMessageDialog(null
                    ,"Debe ingresar el nombre del producto");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void txtBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyTyped
        if(!"".equals(txtBuscar.getText()))
            buscarEvent();
        else 
            llenarLista();
    }//GEN-LAST:event_txtBuscarKeyTyped

    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        if(!"".equals(txtBuscar.getText()))
            buscarEvent();
        else 
            llenarLista();
    }//GEN-LAST:event_txtBuscarKeyReleased

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int indice = lstProductos.getSelectedIndex();
        if(indice!=-1){
            buscarEliminar(indice);   
            mostrarMensaje("Producto eliminado");
        }
        else 
        {
            mostrarMensaje("Seleccione un producto de la lista");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

