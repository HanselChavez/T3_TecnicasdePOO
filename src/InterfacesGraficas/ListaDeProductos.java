    public void llenarLista(){
        lstProductos.setModel(modeloLista);
        modeloLista.removeAllElements();
        listaProductos.forEach((producto) -> {
            modeloLista.addElement(producto.toString());
        });    
    }
     private void buscarEvent() {
        String buscar =txtBuscar.getText().toLowerCase();
        modeloLista.removeAllElements();
        listaProductos.stream().filter((producto) -> (producto.getNombre()
                .toLowerCase().contains(buscar))).forEachOrdered((producto) -> {
            modeloLista.addElement(producto.toString());
        });
        
    }
    private void buscarEliminar(int indice){
        Producto producto = listaProductos.get(indice);
        String nombre = lstProductos.getSelectedValue();
        if(producto.toString().equals(nombre)){
            listaProductos.remove(indice);
            llenarLista();
        }
        
        
    }
    public void agregarProducto(Producto product){
        if(product != null)
            this.listaProductos.add(product);
        else 
            mostrarMensaje("No es posible agregar el producto.");
    }
    public void mostrarMensaje(String msg){
        JOptionPane.showMessageDialog(null, msg);
    }
